// âœ… Render ë°°í¬ëœ ë°±ì—”ë“œ URL ì„¤ì • (í™˜ê²½ ë³€ìˆ˜ ì§€ì›)
const BACKEND_URL =
  process.env.EXPO_PUBLIC_BACKEND_URL || "https://textmemo.onrender.com";

export default function App() {
  const [imageUri, setImageUri] = useState(null);
  const [extractedText, setExtractedText] = useState("");
  const [loading, setLoading] = useState(false);

  // ğŸ“Œ 1ï¸âƒ£ ì´ë¯¸ì§€ ì„ íƒ
  const pickImage = async () => {
    const result = await ImagePicker.launchImageLibraryAsync({
      mediaTypes: ImagePicker.MediaTypeOptions.Images,
      allowsEditing: true,
      quality: 1,
    });
    if (!result.canceled) {
      setImageUri(result.assets[0].uri);
      setExtractedText(""); // ìƒˆë¡œìš´ ì´ë¯¸ì§€ ì„ íƒ ì‹œ ê¸°ì¡´ OCR ê²°ê³¼ ì´ˆê¸°í™”
    }
  };

  // ğŸ“Œ 2ï¸âƒ£ ì´ë¯¸ì§€ ì—…ë¡œë“œ ë° OCR ì²˜ë¦¬
  const uploadImage = async () => {
    if (!imageUri) {
      Alert.alert("Error", "ì´ë¯¸ì§€ë¥¼ ë¨¼ì € ì„ íƒí•´ì£¼ì„¸ìš”.");
      return;
    }

    setLoading(true);

    try {
      // âœ… FormData ìƒì„±
      const formData = new FormData();
      const file = {
        uri: imageUri,
        type: "image/jpeg",
        name: "photo.jpg",
      };

      if (Platform.OS === "web") {
        const response = await fetch(imageUri);
        const blob = await response.blob();
        formData.append("image", blob, "photo.jpg");
      } else {
        formData.append("image", file);
      }

      // âœ… í—¤ë” ì„¤ì • ì¶”ê°€ (React Native ë° ì›¹ í˜¸í™˜)
      const headers = {
        "Content-Type": "multipart/form-data",
      };

      // âœ… ì´ë¯¸ì§€ ì—…ë¡œë“œ ìš”ì²­
      const uploadResponse = await axios.post(
        `${BACKEND_URL}/api/upload`,
        formData,
        { headers }
      );

      console.log("âœ… Upload Success:", uploadResponse.data);
      setExtractedText(uploadResponse.data.text);
    } catch (error) {
      console.error("âŒ Upload Failed:", error.response);
      Alert.alert("ì—…ë¡œë“œ ì‹¤íŒ¨", "íŒŒì¼ì„ ì—…ë¡œë“œí•˜ëŠ” ì¤‘ ì˜¤ë¥˜ê°€ ë°œìƒí–ˆìŠµë‹ˆë‹¤.");
    } finally {
      setLoading(false);
    }
  };

  return (
    <ScrollView contentContainerStyle={styles.container}>
      {/* ì´ë¯¸ì§€ ì„ íƒ ë²„íŠ¼ */}
      <TouchableOpacity onPress={pickImage} style={styles.button}>
        <Text style={styles.buttonText}>ì´ë¯¸ì§€ ì„ íƒ</Text>
      </TouchableOpacity>

      {/* ì„ íƒí•œ ì´ë¯¸ì§€ ë¯¸ë¦¬ë³´ê¸° */}
      {imageUri && <Image source={{ uri: imageUri }} style={styles.image} />}

      {/* OCR ì‹¤í–‰ ë²„íŠ¼ */}
      <TouchableOpacity onPress={uploadImage} style={styles.button}>
        <Text style={styles.buttonText}>
          {loading ? "ì²˜ë¦¬ ì¤‘..." : "í…ìŠ¤íŠ¸ ì¶”ì¶œ"}
        </Text>
      </TouchableOpacity>

      {/* ë¡œë”© ì¸ë””ì¼€ì´í„° */}
      {loading && <ActivityIndicator size="large" color="#fff" />}

      {/* OCR ê²°ê³¼ ì¶œë ¥ */}
      <TextInput
        style={styles.textInput}
        multiline
        value={extractedText}
        onChangeText={setExtractedText}
        placeholder="ì¶”ì¶œëœ í…ìŠ¤íŠ¸ê°€ ì—¬ê¸°ì— í‘œì‹œë©ë‹ˆë‹¤."
        placeholderTextColor="#999"
      />
    </ScrollView>
  );
}

